package code_listings.ch_04_loops_and_files;

public class IncrementDecrement {
    public static void main(String[] args) {
        int no = 5;
        System.out.println("number v.0: " + no);
        System.out.println("\tnumber is incremented");
        no++;
        System.out.println("no v.1: " + no);
        System.out.println("\tnumber is decremented");
        no--;
        System.out.println("no v.2: " + no);
    }
}
