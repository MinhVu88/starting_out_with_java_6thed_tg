package code_listings.ch_04_loops_and_files;

public class Prefix {
    public static void main(String[] args) {
        int no = 10;
        System.out.println("no v.0: " + no);
        System.out.println("\tno is incremented");
        ++no;
        System.out.println("no v.1: " + no);
        System.out.println("\tno is decremented");
        --no;
        System.out.println("no v.2: " + no);
    }
}
