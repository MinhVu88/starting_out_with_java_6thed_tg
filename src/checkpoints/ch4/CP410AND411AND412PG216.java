package checkpoints.ch4;

public class CP410AND411AND412PG216 {
	public static void main(String[] args) {
		// Write a for loop that displays your name 10 times
		for(int count = 0; count < 10; count++) {
			System.out.println("lucifer");
		}
		
		System.out.println();
		
		// Write a for loop that displays all of the odd numbers, 1 through 49
		for(int oddNo = 1; oddNo <= 49; oddNo += 2) {
			System.out.println(oddNo);
		}
		
		System.out.println();
		
		// Write a for loop that displays every fifth number, zero through 100
		for(int fifthNo = 5; fifthNo <= 95; fifthNo += 10) {
			System.out.println(fifthNo + "th");
		}
	}
}
